package eden.project.model;

public class RestaurantProfile {
	
	private String resName;
	private String resEmail;
	private String resPhoneNo;
	private String resAddress;
	private String isAutoAssign;
	private String daysClose;
	private String resOpenTime;
	private String resCloseTime;
	
	public String getResName() {
		return resName;
	}
	public void setResName(String resName) {
		this.resName = resName;
	}
	public String getResEmail() {
		return resEmail;
	}
	public void setResEmail(String resEmail) {
		this.resEmail = resEmail;
	}
	public String getResPhoneNo() {
		return resPhoneNo;
	}
	public void setResPhoneNo(String resPhoneNo) {
		this.resPhoneNo = resPhoneNo;
	}
	public String getResAddress() {
		return resAddress;
	}
	public void setResAddress(String resAddress) {
		this.resAddress = resAddress;
	}
	public String getIsAutoAssign() {
		return isAutoAssign;
	}
	public void setIsAutoAssign(String isAutoAssign) {
		this.isAutoAssign = isAutoAssign;
	}
	public String getDaysClose() {
		return daysClose;
	}
	public void setDaysClose(String daysClose) {
		this.daysClose = daysClose;
	}
	public String getResOpenTime() {
		return resOpenTime;
	}
	public void setResOpenTime(String resOpenTime) {
		this.resOpenTime = resOpenTime;
	}
	public String getResCloseTime() {
		return resCloseTime;
	}
	public void setResCloseTime(String resCloseTime) {
		this.resCloseTime = resCloseTime;
	}
}
